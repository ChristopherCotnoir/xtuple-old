       NULL,
       cohead_curr_id,
       cohead_orderdate,
       NULL,
       NULL,
       NULL,
       NULL,
       cohead_taxzone_id,
       cohead_warehous_id,
       cohead_shipvia,
       cohead_shipto_id,
       cohead_shiptoaddress1,
       cohead_shiptoaddress2,
       cohead_shiptoaddress3,
       cohead_shiptocity,
       cohead_shiptostate,
       cohead_shiptozipcode,
       cohead_shiptocountry,
       cohead_freight,
       cohead_misc,
       cohead_misc_descrip,
       cohead_freight_taxtype_id,
       cohead_misc_taxtype_id,
       cohead_misc_discount,
       cohead_tax_exemption
  FROM cohead
UNION ALL
SELECT 'COB',
       cobmisc_id,
       cohead_number,
       NULL,
       cohead_cust_id,
       NULL,
       cohead_curr_id,
       cohead_orderdate,
       NULL,
       NULL,
       NULL,
